export function generateNewsItems(startId, count) {
  return Array.from({ length: count }, (_, i) => {
    const id = startId + i;
    const idx = (id - 1) % TITLES.length;
    return {
      id: String(id),
      title: TITLES[idx],
      description: DESCRIPTIONS[idx],
      category: CATEGORIES[(id - 1) % CATEGORIES.length],
      date: '03 квітня 2026',
      image: `https://picsum.photos/seed/lab2n${id}/600/300`,
    };
  });
}
